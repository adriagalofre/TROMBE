function [F] = factors_visio_Pool(L_1,L_2,L_3,L_4,A_1,A_2,A_3,A_4,d1,d2)
	  F11=0;
	  F12=((d1+d2)-(L_3+L_2))/(2*A_1);
    F13=((L_1+L_3)-d1)/(2*A_1);
    F14=1-F11-F12-F13;
    F21=(A_1/A_2)*F12;
    F22=0;
    F23=((d1+d2)-(L_4+L_1))/(2*A_2);
    F24=1-F21-F22-F23;
    F31=(A_1/A_3)*F13;
    F32=(A_2/A_3)*F23;
    F33=0;
    F34=1-F31-F32-F33;
    F41=(A_1/A_4)*F14;
    F42=(A_2/A_4)*F24;
    F43=(A_3/A_4)*F34;
    F44=0;
    F=[F11 F12 F13 F14; F21 F22 F23 F24; F31 F32 F33 F34; F41 F42 F43 F44];
    
	end